// Properties of Lorentz transforms:

/* 1. Identity: if v = 0, then you should get out what you put in */
TEST(geometry, lorentz_identity_3D) {
  using namespace spec_3D_Cartesian;
  // LT to co-moving
  {
    Vector vlab{0.0, 0.0, 0.0};
    Geom_T elab{3.14159};
    Vector omega_lab{1.0, 0.0, 0.0};
    EandOmega_T<double> result = LT_to_comoving(vlab, elab, omega_lab);
    EXPECT_EQ(result[0], elab);
    Vector omega_lab_result{result[1], result[2], result[3]};
    EXPECT_EQ(omega_lab_result, omega_lab);
  }
  // LT to lab
  {
    Vector vlab{0.0, 0.0, 0.0};
    Geom_T ecom{3.14159};
    Vector omega_com{1.0, 0.0, 0.0};
    EandOmega_T<double> result = LT_to_lab(vlab, ecom, omega_com);
    EXPECT_EQ(result[0], ecom);
    Vector omega_com_result{result[1], result[2], result[3]};
    EXPECT_EQ(omega_com_result, omega_com);
  }
  return;
}

/* 2. Round-tripping--a transform followed by inverse transform should
 * give back (nearly) the initial energy and direction. Note the tolerances
 * in the soft_equivs: this gives some idea of the numerical sensitivity
 * of these functions. */
TEST(geometry, lorentz_round_trips_3D) {
  using namespace spec_3D_Cartesian;
  for (auto [elab, vlab, omega_lab] : lt_test_cases1) {
    auto com_result = LT_to_comoving(vlab, elab, omega_lab);
    double const ecom = com_result[0];
    Vector omega_com = {com_result[1], com_result[2], com_result[3]};
    auto lab_result = LT_to_lab(vlab, ecom, omega_com);
    double const elab1 = lab_result[0];
    Vector const omega_lab1 = {lab_result[1], lab_result[2], lab_result[3]};
    bool e_ok = soft_equiv_os(elab, elab1, "energy in lab", 2e-14);
    EXPECT_TRUE(e_ok);
    bool o_ok_0 =
        soft_equiv_os(omega_lab[0], omega_lab1[0], "dirn 0 in lab", 9e-14);
    bool o_ok_1 =
        soft_equiv_os(omega_lab[1], omega_lab1[1], "dirn 1 in lab", 9e-14);
    bool o_ok_2 =
        soft_equiv_os(omega_lab[2], omega_lab1[2], "dirn 2 in lab", 1.5e-13);
    EXPECT_TRUE(o_ok_0);
    EXPECT_TRUE(o_ok_1);
    EXPECT_TRUE(o_ok_2);
  }
  return;
} // TEST(geometry, lorentz_round_trips_3D)

